    <script>
        document.addEventListener('DOMContentLoaded', function () {
            // Initialize AOS
            AOS.init({
                duration: 800,
                easing: 'ease-in-out',
                once: true
            });

            // Initialize Tooltips
            var tooltipTriggerList = [].slice.call(document.querySelectorAll('[data-bs-toggle="tooltip"]'));
            tooltipTriggerList.map(function (tooltipTriggerEl) {
                return new bootstrap.Tooltip(tooltipTriggerEl);
            });

            // Smooth scroll for anchor links
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function (e) {
                    const href = this.getAttribute('href');
                    if (href === '#') return;

                    e.preventDefault();
                    const target = document.querySelector(href);
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });

            // Google Reviews Fetching
            async function loadGoogleReviews() {
                const reviewsContainer = document.getElementById('reviews-container');
                const ratingSummary = document.getElementById('google-rating-summary');

                try {
                    const response = await fetch('get-reviews.php');
                    const result = await response.json();

                    if (result.success && result.data && result.data.reviews) {
                        // Render Rating Summary
                        const rating = result.data.rating;
                        const total = result.data.user_ratings_total;
                        let starsHtml = '';
                        for (let i = 1; i <= 5; i++) {
                            if (i <= Math.floor(rating)) {
                                starsHtml += '<i class="fas fa-star text-warning"></i>';
                            } else if (i - 0.5 <= rating) {
                                starsHtml += '<i class="fas fa-star-half-alt text-warning"></i>';
                            } else {
                                starsHtml += '<i class="far fa-star text-warning"></i>';
                            }
                        }

                        if (ratingSummary) {
                            ratingSummary.innerHTML = `
                                <div class="d-flex flex-column align-items-center">
                                    <div class="h3 mb-1">${rating} ${starsHtml}</div>
                                    <div class="text-muted small">Basado en ${total} opiniones en Google</div>
                                </div>
                            `;
                        }

                        // Render Reviews
                        if (reviewsContainer) {
                            reviewsContainer.innerHTML = '';
                            result.data.reviews.forEach((review, index) => {
                                const initials = review.author_name.split(' ').map(n => n[0]).join('').toUpperCase().substring(0, 2);
                                const stars = '<i class="fas fa-star text-warning"></i>'.repeat(review.rating);
                                const timeAgo = formatTimeAgo(review.time);

                                const reviewHtml = `
                                    <div class="col-md-6 col-lg-4" data-aos="fade-up" data-aos-delay="${index * 100}">
                                        <div class="review-card h-100">
                                            <div class="review-header">
                                                <div class="review-avatar">${initials}</div>
                                                <div>
                                                    <h5 class="mb-0">${review.author_name}</h5>
                                                    <small class="text-muted">${timeAgo}</small>
                                                </div>
                                            </div>
                                            <div class="mb-2 text-warning" style="font-size: 0.8rem;">${stars}</div>
                                            <p class="review-text">${review.text}</p>
                                        </div>
                                    </div>
                                `;
                                reviewsContainer.insertAdjacentHTML('beforeend', reviewHtml);
                            });

                            // Re-initialize AOS for new elements
                            if (typeof AOS !== 'undefined') {
                                AOS.refresh();
                            }
                        }
                    } else {
                        if (reviewsContainer) {
                            reviewsContainer.innerHTML = '<div class="col-12 text-center text-muted">No se pudieron cargar las reseñas en este momento.</div>';
                        }
                    }
                } catch (error) {
                    console.error('Error loading reviews:', error);
                    if (reviewsContainer) {
                        reviewsContainer.innerHTML = '<div class="col-12 text-center text-muted">Error conectando con la API de reseñas.</div>';
                    }
                }
            }

            function formatTimeAgo(timestamp) {
                const seconds = Math.floor((new Date() - new Date(timestamp * 1000)) / 1000);
                let interval = seconds / 31536000;
                if (interval > 1) return Math.floor(interval) + (Math.floor(interval) === 1 ? " año atrás" : " años atrás");
                interval = seconds / 2592000;
                if (interval > 1) return Math.floor(interval) + (Math.floor(interval) === 1 ? " mes atrás" : " meses atrás");
                interval = seconds / 86400;
                if (interval > 1) return Math.floor(interval) + (Math.floor(interval) === 1 ? " día atrás" : " días atrás");
                return "Recientemente";
            }

            loadGoogleReviews();
        });
    </script>
